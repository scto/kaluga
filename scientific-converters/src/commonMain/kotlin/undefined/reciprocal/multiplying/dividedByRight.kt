package com.splendo.kaluga.scientific.converter.undefined.reciprocal.multiplying

import com.splendo.kaluga.base.utils.Decimal
import com.splendo.kaluga.scientific.UndefinedQuantityType
import com.splendo.kaluga.scientific.UndefinedScientificValue
import com.splendo.kaluga.scientific.byDividing
import com.splendo.kaluga.scientific.unit.AbstractUndefinedScientificUnit
import com.splendo.kaluga.scientific.unit.UndefinedMultipliedUnit
import com.splendo.kaluga.scientific.unit.UndefinedReciprocalUnit

// Inv<Mul<A, B>> / B -> Inv<Mul<Mul<A, B>, B>>

fun <
    NumeratorReciprocalLeftQuantity : UndefinedQuantityType,
    NumeratorReciprocalLeftUnit : AbstractUndefinedScientificUnit<NumeratorReciprocalLeftQuantity>,
    NumeratorReciprocalRightAndDenominatorQuantity : UndefinedQuantityType,
    NumeratorReciprocalRightUnit : AbstractUndefinedScientificUnit<NumeratorReciprocalRightAndDenominatorQuantity>,
    NumeratorReciprocalUnit : UndefinedMultipliedUnit<
        NumeratorReciprocalLeftQuantity,
        NumeratorReciprocalLeftUnit,
        NumeratorReciprocalRightAndDenominatorQuantity,
        NumeratorReciprocalRightUnit,
        >,
    NumeratorUnit : UndefinedReciprocalUnit<
        UndefinedQuantityType.Multiplying<
            NumeratorReciprocalLeftQuantity,
            NumeratorReciprocalRightAndDenominatorQuantity,
            >,
        NumeratorReciprocalUnit,
        >,
    DenominatorUnit : AbstractUndefinedScientificUnit<NumeratorReciprocalRightAndDenominatorQuantity>,
    TargetReciprocalUnit : UndefinedMultipliedUnit<
        UndefinedQuantityType.Multiplying<
            NumeratorReciprocalLeftQuantity,
            NumeratorReciprocalRightAndDenominatorQuantity,
            >,
        NumeratorReciprocalUnit,
        NumeratorReciprocalRightAndDenominatorQuantity,
        NumeratorReciprocalRightUnit,
        >,
    TargetUnit : UndefinedReciprocalUnit<
        UndefinedQuantityType.Multiplying<
            UndefinedQuantityType.Multiplying<
                NumeratorReciprocalLeftQuantity,
                NumeratorReciprocalRightAndDenominatorQuantity,
                >,
            NumeratorReciprocalRightAndDenominatorQuantity,
            >,
        TargetReciprocalUnit,
        >,
    TargetValue : UndefinedScientificValue<
        UndefinedQuantityType.Reciprocal<
            UndefinedQuantityType.Multiplying<
                UndefinedQuantityType.Multiplying<
                    NumeratorReciprocalLeftQuantity,
                    NumeratorReciprocalRightAndDenominatorQuantity,
                    >,
                NumeratorReciprocalRightAndDenominatorQuantity,
                >,
            >,
        TargetUnit,
        >,
    > UndefinedScientificValue<
    UndefinedQuantityType.Reciprocal<
        UndefinedQuantityType.Multiplying<
            NumeratorReciprocalLeftQuantity,
            NumeratorReciprocalRightAndDenominatorQuantity,
            >,
        >,
    NumeratorUnit,
    >.dividedByRight(
    right: UndefinedScientificValue<
        NumeratorReciprocalRightAndDenominatorQuantity,
        DenominatorUnit,
        >,
    numeratorReciprocalUnitXNumeratorReciprocalRightUnit: NumeratorReciprocalUnit.(NumeratorReciprocalRightUnit) -> TargetReciprocalUnit,
    reciprocalTargetUnit: TargetReciprocalUnit.() -> TargetUnit,
    factory: (Decimal, TargetUnit) -> TargetValue,
) = unit.inverse.numeratorReciprocalUnitXNumeratorReciprocalRightUnit(
    unit.inverse.right,
).reciprocalTargetUnit().byDividing(this, right, factory)
